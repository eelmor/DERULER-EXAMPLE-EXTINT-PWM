  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_TIM2_Init();
  MX_TIM22_Init();
  /* USER CODE BEGIN 2 */

  // Start PWM output of RGB LED channels
  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_1);
  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_2);
  HAL_TIM_PWM_Start(&htim2, TIM_CHANNEL_3);

  /* USER CODE END 2 */

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {
    /* USER CODE END WHILE */

	  // If change_color is 1, change color
	  if(change_color == 1) {

		  red += red_add; // Increase red by 1 each time, or decrease by 1 if max was reached
		  green += green_add; // Increase green by 2 each time, or decrease by 2 if max was reached
		  blue += blue_add; // Increase blue by 3 each time, or decrease by 3 if max was reached

		  // If any color value is more than 255 limit it to 256. If any color is less than 0, limit it to 0.
		  // Additionally change the sign of the value added to it each cycle to reverse the direction
		  if(red > 255) {
			  red = 256;
			  red_add = -red_add;
		  }
		  if(red < 0) {
			  red = 0;
			  red_add = -red_add;
		  }

		  if(green > 255) {
			  green = 256;
			  green_add = -green_add;
		  }
		  if(green < 0) {
			  green = 0;
			  green_add = -green_add;
		  }

		  if(blue > 255) {
			  blue = 256;
			  blue_add = -blue_add;
		  }
		  if(blue < 0) {
			  blue = 0;
			  blue_add = -blue_add;
		  }


		  // Apply color values to the corresponding PWM channel
		  __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_2, red);
		  __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_1, green);
		  __HAL_TIM_SET_COMPARE(&htim2, TIM_CHANNEL_3, blue);

		  // Wait for 10 ms before repeating
		  HAL_Delay(10);

	  }
    /* USER CODE BEGIN 3 */
  }
